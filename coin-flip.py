# Importing random
import random
# making a list
coin = ["Heads", "Tails"]
# saving the flip as a string variable
flip_a_dee_doo_da = random.choice(coin)
# displaying the variable flip
print(f"You flipped: {flip_a_dee_doo_da}")
# slicing the list as separate variabels
side_1 = coin[0]
side_2 = coin[1]
# printing the side slices
print(f"Side 1 is {side_1}")
print(f"Side 2 is {side_2}")

